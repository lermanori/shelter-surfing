// Helper function to search locations
export const searchIsraeliLocations = (query) => {
  const searchTerm = query.toLowerCase();
  return israeliLocations.filter(location => 
    location.name.toLowerCase().includes(searchTerm) ||
    location.english.toLowerCase().includes(searchTerm)
  ).slice(0, 10); // Limit to 10 results
};

// Helper function to get location by name
export const getLocationByName = (name) => {
  return israeliLocations.find(location => 
    location.name === name || location.english === name
  );
};

// Helper function to get locations by region
export const getLocationsByRegion = (region) => {
  return israeliLocations.filter(location => location.region === region);
};

// Helper function to get locations by type
export const getLocationsByType = (type) => {
  return israeliLocations.filter(location => location.type === type);
}; 